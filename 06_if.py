numero1 = int(input("Ingrese un numero: "))
numero2 = int(input("Ingrese un segundo numero: "))

if numero1 == numero2:
    print(f"El primer numero ingresado {numero1} es igual al segundo numero ingresado {numero2}")
elif numero1 > numero2:
    print(f"El primer numero ingresado {numero1} es mayor que el segundo numero ingresado {numero2}")
else: 
    print(f"El primero numero {numero1} es menor que el segundo numero ingresado {numero2}")
